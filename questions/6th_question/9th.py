def add(a, b):
    """
    Add two numbers and return their sum.
    
    Args:
        a: The first number to add.
        b: The second number to add.
    
    Returns:
        The sum of a and b.
    """
    return a + b

def reverse_text(s):
    """
    Reverse a string.
    
    Args:
        s: The string to reverse.
    
    Returns:
        The reversed string.
    """
    return s[::-1]

